function d(offset: number): string {
  const x = new Date();
  x.setDate(x.getDate() + offset);
  return x.toISOString().slice(0, 10);
}

function ts(offset: number): string {
  const x = new Date();
  x.setDate(x.getDate() + offset);
  return x.toISOString();
}

