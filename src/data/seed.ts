function line(
  id: string,
  p: Product,
  qty: number,
  price: number,
  expiry?: string
) {
  return {
    id,
    productId: p.id,
    productName: p.name,
    unit: p.unit,
    quantity: qty,
    price,
    expiryDate: expiry,
    subtotal: qty * price,
  };
}

